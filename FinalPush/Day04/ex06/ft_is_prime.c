int		ft_is_prime(int nb)
{
	int		x;

	x = 2;
	while (nb % x != 0 && x <= nb)
		x++;
	return (nb > 0 && nb != 1 && (nb == 2 || nb % 2 != 0)
		&& (nb % x != 0 || nb == x) ? 1 : 0);
}
